public class GUIModel {          //logic adding numbers
    public static int addNumbers(int num1, int num2){
    return num1+num2;
    
   
    }
    public static int subNumbers(int num1, int num2){
    return num1-num2;
    
   
    }
    public static int multiplyNumbers(int num1, int num2){
    return num1*num2;
    
   
    }
    public static int divideNumbers(int num1, int num2){
    return num1/num2;
    
   
    }
    public static int remainderNumbers(int num1, int num2){
    return num1%num2;
    
   
    }
    public static int resetNumbers(int num1, int num2){
    return num1;
   
    
    
    
    
   
    }
    
    
    
    
    
}